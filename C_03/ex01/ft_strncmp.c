int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	t;

	t = 0;
	while (s1[t] != '\0' && s2[t] != '\0' && t < n - 1 && s1[t] == s2[t])
	{
		t++;
	}
	if (n == 0)
		return (0);
	return (s1[t] - s2[t]);
}
