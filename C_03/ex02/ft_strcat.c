char	*ft_strcat(char *dest, char *src)
{
	int	h;
	int	b;

	b = 0;
	h = 0;
	while (dest[b] != '\0')
	{
		b++;
	}
	while (src[h] != '\0')
	{
		dest[b + h] = src[h];
		h++;
	}
	dest[b + h] = '\0';
	return (dest);
}
