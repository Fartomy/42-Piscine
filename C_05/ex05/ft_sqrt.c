#include <stdio.h>

int	ft_sqrt(int nb)
{
	int m;

	m = 1;
	while(m * m != nb)
	{
		if (m > nb || nb > 2147483647)
			return(0);
		m++;
	}
	return(m);
}

int main(void)
{
	printf("%d", ft_sqrt(9));
	return(0);
}
