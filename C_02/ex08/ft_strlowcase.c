char	*ft_strlowcase(char *str)
{
	int	h;

	h = 0;
	while (str[h] != '\0')
	{
		if (str[h] >= 'A' && str[h] <= 'Z')
			str[h] += 32;
		h++;
	}
	return (str);
}
