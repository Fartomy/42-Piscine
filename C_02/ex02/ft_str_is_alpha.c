int	ft_str_is_alpha(char *str)
{
	int	z;

	z = 0;
	while (str[z] != '\0')
	{
		if ((str[z] < 'a' || str[z] > 'z') && (str[z] < 'A' || str[z] > 'Z'))
		{
			return (0);
		}
		z++;
	}
	return (1);
}
